#Programa para solicitar dois números inteiros e calcular a soma dos mesmos.

#Entra de dados e declaração de variáveis
num1 = int(input("Insira um número inteiro: "))
num2 = int(input("Insirra outro número inteiro: "))

#Processamento(cálculo)
soma = num1 + num2

#Saída
print(f"O resultado da soma dos dois números informados é: {soma}")
